// EEPROM write function
HAL_StatusTypeDef eeprom_write(uint16_t mem_address, uint8_t *data, uint16_t size)
{
    HAL_StatusTypeDef status = HAL_I2C_Mem_Write(&hi2c1, EEPROM_ADDRESS, mem_address, I2C_MEMADD_SIZE_16BIT, data, size, HAL_MAX_DELAY);
    HAL_Delay(5); // Ensure the write operation is complete
    return status;
}

// EEPROM read function
HAL_StatusTypeDef eeprom_read(uint16_t mem_address, uint8_t *data, uint16_t size)
{
    return HAL_I2C_Mem_Read(&hi2c1, EEPROM_ADDRESS, mem_address, I2C_MEMADD_SIZE_16BIT, data, size, HAL_MAX_DELAY);
}
